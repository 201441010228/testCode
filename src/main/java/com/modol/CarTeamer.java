public class CarTeamer {
    private String cityName;
    private String categery;
    private String position;
    private String name;
    private Long mobile;
    private String mail;
    private String companyName;
    private Long companyId;
    private String carTeamName;
    private Long carTeamId;

    public String getCityName() {
        return cityName;
    }

    public void setCityName(String cityName) {
        this.cityName = cityName;
    }

    public String getCategery() {
        return categery;
    }

    public void setCategery(String categery) {
        this.categery = categery;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getMobile() {
        return mobile;
    }

    public void setMobile(Long mobile) {
        this.mobile = mobile;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public Long getCompanyId() {
        return companyId;
    }

    public void setCompanyId(Long companyId) {
        this.companyId = companyId;
    }

    public String getCarTeamName() {
        return carTeamName;
    }

    public void setCarTeamName(String carTeamName) {
        this.carTeamName = carTeamName;
    }

    public Long getCarTeamId() {
        return carTeamId;
    }

    public void setCarTeamId(Long carTeamId) {
        this.carTeamId = carTeamId;
    }
}
